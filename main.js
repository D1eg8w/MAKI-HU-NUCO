document.addEventListener("click", function (e) {
    // Ver más
    if (e.target.classList.contains("producto-vermas")) {
        const banner = document.getElementById('banner');
        const id = e.target.getAttribute("data-id");
        const producto = productos.find(p => p.id === id);

        if (producto) {
            contenedorProductos.style.display = "none";
            detalleProducto.style.display = "block";
            banner.classList.add('hidden');

            detalleProducto.innerHTML = `
                <h2>${producto.titulo}</h2>
                <img class="producto-imagen" src="${producto.imagen}" alt="${producto.titulo}">
                <p>${producto.descripcion || "Descripción no disponible."}</p>
                <p class="producto-artesano">Artesano o Empresa: ${producto.artesano}</p>
                <a class="producto-contactar" href="https://wa.me/${producto.numero}?text=Hola,%20quiero%20información%20sobre%20${encodeURIComponent(producto.titulo)}" target="_blank">
                    Contactar
                </a>
                <button id="volver">← Volver</button>
            `;
        }
    }

    // Volver
    if (e.target.id === "volver") {
        detalleProducto.style.display = "none";
        contenedorProductos.style.display = "";
        const banner = document.getElementById('banner');
        const botonTodosActivo = document.getElementById('todos').classList.contains('active');
        const hayBusqueda = document.querySelector("#buscador").value.trim() !== "";
        
        if (botonTodosActivo && !hayBusqueda) {
            banner.classList.remove('hidden');
        }
    }
});

