/* 
===========================================
2. FUNCIONALIDAD DE BÚSQUEDA
===========================================
*/
function filtrarProductos(termino) {
    const banner = document.getElementById('banner');
    
    if (termino.trim() === "") {
        productosFiltrados = productos;
        limpiarBusqueda.classList.remove("show");
        banner.classList.remove('hidden');
    } else {
        productosFiltrados = productos.filter(producto => 
            producto.titulo.toLowerCase().includes(termino.toLowerCase()) ||
            producto.categoria.nombre.toLowerCase().includes(termino.toLowerCase())
        );
        limpiarBusqueda.classList.add("show");
        banner.classList.add('hidden');
    }
    
    tituloPrincipal.innerText = termino.trim() === "" 
        ? "Todos los productos" 
        : `Resultados para: "${termino}"`;
    
    cargarProductos(productosFiltrados);
    botonesCategorias.forEach(boton => boton.classList.remove("active"));
}

buscador.addEventListener("input", (e) => {
    filtrarProductos(e.target.value);
});

buscador.addEventListener("keypress", (e) => {
    if (e.key === "Enter") e.preventDefault();
});

limpiarBusqueda.addEventListener("click", () => {
    buscador.value = "";
    filtrarProductos("");
    buscador.focus();
});

/* 
===========================================
3. MANEJO DE CATEGORÍAS
===========================================
*/
function cambiarCategoria(botonClickeado) {
    const banner = document.getElementById('banner');
    resetearScrollMovil();
    buscador.value = "";
    limpiarBusqueda.classList.remove("show");

    if (detalleProducto.style.display === "block") {
        detalleProducto.style.display = "none";
        contenedorProductos.style.display = "";
    }

    botonesCategorias.forEach(boton => boton.classList.remove("active"));
    botonClickeado.classList.add("active");

    if (botonClickeado.id === "nosotros") {
        tituloPrincipal.innerText = "Nosotros";
        contenedorProductos.style.display = "none";
        detalleProducto.style.display = "none";
        seccionNosotros.style.display = "block";
        banner.classList.add('hidden');
        return;
    } else {
        seccionNosotros.style.display = "none";
        contenedorProductos.style.display = "";
    }

    if (botonClickeado.id !== "todos") {
        const productoCategoria = productos.filter(producto => producto.categoria.id === botonClickeado.id);
        tituloPrincipal.innerText = productoCategoria[0]?.categoria.nombre || "";
        cargarProductos(productoCategoria);
        banner.classList.add('hidden');
    } else {
        tituloPrincipal.innerText = "Todos los productos";
        cargarProductos(productos);
        banner.classList.remove('hidden');
    }

    if (window.innerWidth <= 600) {
        setTimeout(() => {
            window.scrollTo({ top: 0, behavior: 'smooth' });
        }, 100);
    }
}

botonesCategorias.forEach(boton => {
    boton.addEventListener("click", (e) => {
        e.preventDefault();
        const aside = document.querySelector('.nav-principal');
        const body = document.body;
        
        if (aside && aside.classList.contains('aside-visible')) {
            aside.classList.remove('nav-principal-visible');
            body.classList.remove('menu-open');
            setTimeout(() => cambiarCategoria(e.currentTarget), 300);
        } else {
            cambiarCategoria(e.currentTarget);
        }
    });
});

