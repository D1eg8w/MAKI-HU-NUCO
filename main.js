function cargarProductos(productosElegidos) {
    contenedorProductos.innerHTML = "";
    
    if (productosElegidos.length === 0) {
        contenedorProductos.innerHTML = `
            <div class="sin-resultados">
                <i class="bi bi-search" style="font-size: 3rem; margin-bottom: 1rem; display: block;"></i>
                <p>No se encontraron productos que coincidan con tu búsqueda.</p>
            </div>`;
        return;
    }

    productosElegidos.forEach(producto => {
        const div = document.createElement("div");
        div.classList.add("producto");
        div.innerHTML = `
            <div class="producto-detalles">
                <img class="producto-imagen" src="${producto.imagen}" alt="${producto.titulo}">
                <h3 class="producto-titulo">${producto.titulo}</h3>
                <a class="producto-contactar" href="https://wa.me/${producto.numero}?text=Hola,%20quiero%20información%20sobre%20${encodeURIComponent(producto.titulo)}" target="_blank">
                    Contactar
                </a>
                <button class="producto-vermas" data-id="${producto.id}">Ver más</button>
            </div>
        `;
        contenedorProductos.append(div);
    });
}

